package JongeunStudy;

public class StudyEx24 {

	public static void main(String[] args) {

		int a = 10;

		a++; // a = a+1과 동일하다.
		System.out.printf(" a ++ ==> %d \n", a);

		a--; // a = a-1과 동일하다.
		System.out.printf(" a -- == %d \n", a);

		a += 5; // a = a+5와 동일하다.
		System.out.printf(" a += 5 ==> %d \n", a);

		a -= 5; // a = a-5와 동일하다.
		System.out.printf(" a -= 5 ==> %d \n", a);

		a *= 5; // a = a*5와 동일하다.
		System.out.printf(" a *= 5 ==> %d \n", a);

		a /= 5; // a = a/5와 동잏하다.
		System.out.printf(" a /= 5 ==> %d \n", a);

		a %= 5; // a = a%5와 동일하다.
		System.out.printf(" a %%= 5 ==> %d \n", a);
		
		
	}

}
